    public void proveedorl() {
        try {
            proveedor = proImp.lista_usuarios();
            for (Proveedor usr : proveedor) {
                Object[] o = new Object[5];
                o[0] = usr.getId();
                o[1] = usr.getNombre();
                o[2] = usr.getProducto();
                o[3] = usr.getTelefono();
                o[4] = usr.getDireccion();

                modelo.addRow(o);
            }
        } catch (Exception e) {
            System.out.println("Error al mostrar los datos en la tabla");
        }
    }

