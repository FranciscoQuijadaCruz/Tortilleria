    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed

        if (!tblProveedor.getSelectionModel().isSelectionEmpty()) {
            pro = proveedor.get(tblProveedor.getSelectedRow());
            if (JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea eliminar al proveedor " + pro.getProducto() + "?", "Quitar producto", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0) {
                try {
                    if (proImp.Eliminar(pro)) {
                        JOptionPane.showMessageDialog(null, "Se ha eliminado el proveedor con éxito", "Proveedor eliminado", JOptionPane.INFORMATION_MESSAGE);
                        modelo = new DefaultTableModel();
                        tblProveedor.setModel(modelo);
                        modelo.setColumnIdentifiers(titulos);
                        proveedor = proImp.lista_usuarios();
                        for (Proveedor prodt : proveedor) {
                            Object[] o = new Object[5];
                            o[0] = prodt.getId();
                            o[1] = prodt.getNombre();
                            o[2] = prodt.getProducto();
                            o[3] = prodt.getTelefono();
                            o[4] = prodt.getDireccion();
                            modelo.addRow(o);
                        }

                    }
                } catch (Exception e) {
                }
            }
        }
        modelo = new DefaultTableModel();
        tblProveedor.setModel(modelo);
        modelo.setColumnIdentifiers(titulos);
        txtNombre.setText("");
        txtProducto.setText("");
        txtTelefono.setText("");
        txtDirecion.setText("");
        PlaceHolder holder = new PlaceHolder(txtNombre, "Nombre del proveedor");
        PlaceHolder holder1 = new PlaceHolder(txtProducto, "Productos que surte");
        PlaceHolder holder2 = new PlaceHolder(txtTelefono, "Teléfono para contactar");
        PlaceHolder holder3 = new PlaceHolder(txtDirecion, "Ingresar su dirección");
        proveedorl();
    }//GEN-LAST:event_btnQuitarActionPerformed

