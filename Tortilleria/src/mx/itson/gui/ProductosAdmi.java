    public void productosl() {
        try {
            productos = proImp.lista_productos();
            for (Producto pro : productos) {
                Object[] o = new Object[4];
                o[0] = pro.getId();
                o[1] = pro.getProducto();
                o[2] = "$ " + pro.getPrecio();

                modelo.addRow(o);
            }
        } catch (Exception e) {
            System.out.println("Error al mostrar los datos en la tabla");
        }
    }

