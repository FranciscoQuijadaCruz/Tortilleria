    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (txtNombre.getText().isEmpty() || txtPrecio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Los siguientes campos no pueden estar vacíos: \nProducto\nPrecio", "Datos incompletos", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                Producto prodt = new Producto();
                prodt.setProducto(txtNombre.getText());
                prodt.setPrecio(Double.parseDouble(txtPrecio.getText()));
                ProductoInt proImp = new ProductoImp();
                if (proImp.Agregar(prodt)) {
                    JOptionPane.showMessageDialog(null, "Se registró el producto", "Producto registrado", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
            }
        }
        modelo = new DefaultTableModel();
        tbProductos.setModel(modelo);
        modelo.setColumnIdentifiers(titulos);
        productosl();
        txtNombre.setText("");
        txtPrecio.setText("");
        PlaceHolder holder1 = new PlaceHolder(txtNombre, "Nombre del producto");
        PlaceHolder holder3 = new PlaceHolder(txtPrecio, "Precio del producto");
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnQuitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQuitarActionPerformed

        if (!tbProductos.getSelectionModel().isSelectionEmpty()) {
            pro = productos.get(tbProductos.getSelectedRow());
            if (JOptionPane.showConfirmDialog(null, "¿Esta seguro que desea eliminar el producto " + pro.getProducto() + "?", "Quitar producto", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == 0) {
                try {
                    if (proImp.Eliminar(pro)) {
                        JOptionPane.showMessageDialog(null, "Se ha eliminado el producto con éxito", "Producto eliminado", JOptionPane.INFORMATION_MESSAGE);
                        modelo = new DefaultTableModel();
                        tbProductos.setModel(modelo);
                        modelo.setColumnIdentifiers(titulos);
                        productos = proImp.lista_productos();
                        for (Producto prodt : productos) {
                            Object[] o = new Object[4];
                            o[0] = prodt.getId();
                            o[1] = prodt.getProducto();
                            o[2] = prodt.getPrecio();
                            modelo.addRow(o);
                        }

                    }
                } catch (Exception e) {
                }
            }
        }
        modelo = new DefaultTableModel();
        tbProductos.setModel(modelo);
        modelo.setColumnIdentifiers(titulos);
        productosl();
        txtNombre.setText("");
        txtPrecio.setText("");
        PlaceHolder holder1 = new PlaceHolder(txtNombre, "Nombre del producto");
        PlaceHolder holder3 = new PlaceHolder(txtPrecio, "Precio del producto");
    }//GEN-LAST:event_btnQuitarActionPerformed

